"""All the functions."""

def Add(a:float, b:float) -> float:
    """Add the values.

    Args:
        - a (float): first input value for function
        - b (float): second input value for function
    Returns:
        - value (float): addition of two inputs
    """
    return a+b

def Subtract(a:float, b:float) -> float:
    """Subtract the values.

    Args:
        - a (float): first input value for function
        - b (float): second input value for function
    Returns:
        - value (float): Subtract of two inputs
    """
    return a-b